    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        Libro lb = new Libro();
        lb.setId_libro(Integer.parseInt(txtID.getText()));
        lb.setTitulo(txtNombre.getText());
        lb.setAutor(txtAutor.getText());
        lb.setCategoria_id(Integer.parseInt(txtCategoria.getText()));
        if(!"".equals(txtID.getText())&&!"".equals(txtNombre.getText())&&!"".equals(txtAutor.getText())&&!"".equals(txtCategoria.getText())){
            JOptionPane.showMessageDialog(this,"Datos ingresados correctamente","",JOptionPane.INFORMATION_MESSAGE);
            bd.insertarLibros(lb);
            LimpiarForm();
            ListarDatos();
            Limpiar();
        }else{
            JOptionPane.showMessageDialog(this, "Faltan datos por ingresar","",JOptionPane.ERROR_MESSAGE);
        }   
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Connection cn = null;
        
        try{
            cn = getConnection();
            ps = cn.prepareStatement("SELECT * FROM LIBRO WHERE ID_LIBRO = ?");
            ps.setString(1, txtID.getText());
            
            rs = ps.executeQuery();
            
            if(rs.next()){
                txtID.setText(rs.getString("ID_LIBRO"));
                txtNombre.setText(rs.getString("TITULO"));
                txtAutor.setText(rs.getString("AUTOR"));
                txtCategoria.setText(rs.getString("CATEGORIA_ID"));
            }else{
                JOptionPane.showMessageDialog(null, "No existe ningÃºn libro con dicho ID");
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Connection cn = null;
        
        try{
            cn = getConnection();
            ps = cn.prepareStatement("UPDATE LIBRO SET TITULO = ?, AUTOR = ?, CATEGORIA_ID = ?"
                    + "WHERE ID_LIBRO = ?");
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtAutor.getText());
            ps.setString(3, txtCategoria.getText());
            ps.setString(4, txtID.getText());
            
            int res = ps.executeUpdate();
            
            if(res > 0){
                JOptionPane.showMessageDialog(null, "Libro modificado correctamente");
            }else{
                JOptionPane.showMessageDialog(null, "Error a la hora de modificar!");
            }
            
            LimpiarForm();
            
            Limpiar();
            cn.close();
            
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

